package com.cimc.senheyun.utils.jo;

public class TokenJo {

	private AccessTokenJo data;	
	private String code;
	private String msg;
	
	public AccessTokenJo getData() {
		return data;
	}
	public void setData(AccessTokenJo data) {
		this.data = data;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getMsg() {
		return msg;
	}
	public void setMsg(String msg) {
		this.msg = msg;
	}
	
}
